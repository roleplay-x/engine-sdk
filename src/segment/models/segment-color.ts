/**
 * Color configuration for segment display
 * @export
 * @interface SegmentColor
 */
export interface SegmentColor {
  /**
   * Background color in hex format (e.g., #FF5733)
   * @type {string}
   * @memberof SegmentColor
   */
  background: string;
  /**
   * Text color in hex format (e.g., #FFFFFF)
   * @type {string}
   * @memberof SegmentColor
   */
  text: string;
}
