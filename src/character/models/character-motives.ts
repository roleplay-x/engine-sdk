/**
 *
 * @export
 * @interface CharacterMotives
 */
export interface CharacterMotives {
  /**
   *
   * @type {number}
   * @memberof CharacterMotives
   */
  hunger: number;
  /**
   *
   * @type {number}
   * @memberof CharacterMotives
   */
  thirst: number;
  /**
   *
   * @type {number}
   * @memberof CharacterMotives
   */
  energy: number;
  /**
   *
   * @type {number}
   * @memberof CharacterMotives
   */
  health: number;
  /**
   *
   * @type {number}
   * @memberof CharacterMotives
   */
  mood: number;
  /**
   *
   * @type {boolean}
   * @memberof CharacterMotives
   */
  isInCriticalState: boolean;
  /**
   *
   * @type {boolean}
   * @memberof CharacterMotives
   */
  isHealthy: boolean;
}
